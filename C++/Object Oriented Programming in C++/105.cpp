#include <iostream>
using namespace std;
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Base
{
public:
    virtual void show()
    {
        cout << "Base\n";
    }
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Derived1 : public Base
{
public:
    void show()
    {
        cout << "Derived1\n";
    }
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Derived2 : public Base
{
public:
    void show()
    {
        cout << "Derived2\n";
    }
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    Derived1 dv1;
    Derived2 dv2;
    Base *pointer;
    pointer = &dv1;
    pointer->show();
    pointer = &dv2;
    pointer->show();
    return 0;
}