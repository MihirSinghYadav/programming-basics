#include <iostream>
using namespace std;
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class gamma
{
private:
    static int total;
    int id;

public:
    gamma()
    {
        total++;
        id = total;
    }
    ~gamma()
    {
        total--;
        cout << "Destroying ID number" << id << endl;
    }
    static void showTotal()
    {
        cout << "Total is " << total << endl;
    }
    void showId()
    {
        cout << "ID number is " << id << endl;
    }
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int gamma::total = 0;
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    gamma g1;
    gamma::showTotal();

    gamma g2, g3;
    gamma::showTotal();

    g1.showId();
    g2.showId();
    g3.showId();
    cout << "--------------End of Program------------------\n";
    return 0;
}