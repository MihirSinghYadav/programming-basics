#include <iostream>
using namespace std;
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Distance
{
private:
    int feet;
    float inches;

public:
    Distance() : feet(0), inches(0.0)
    {
    }
    Distance(int ft, float in) : feet(ft), inches(in)
    {
    }
    void showDistance()
    {
        cout << feet << "\'-" << inches << '\"';
    }
    friend float square(Distance);
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
float square(Distance d)
{
    float fltfeet = d.feet + d.inches / 12;
    float feet_squared = fltfeet * fltfeet;
    return feet_squared;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    Distance dist(3, 9.0);
    float sq_ft;
    sq_ft = square(dist);
    cout << "\nDistance = ";
    dist.showDistance();
    cout << "\nSquare  = " << sq_ft << " square feet\n";
    return 0;
}