#include <iostream>
using namespace std;
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Base
{
public:
    virtual void show() = 0;
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Derived1 : public Base
{
public:
    void show()
    {
        cout << "Derived1\n";
    }
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Derived2 : public Base
{
public:
    void show()
    {
        cout << "Derived2\n";
    }
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    //Base bad;
    Base *arr[2];

    Derived1 dv1;
    Derived2 dv2;

    arr[0] = &dv1;
    arr[1] = &dv2;

    arr[0]->show();
    arr[1]->show();
    return 0;
}